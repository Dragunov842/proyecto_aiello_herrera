    <p style="color:white">
    En Leblanc, creemos que el café es mucho más que una bebida, es un puente entre las personas, un refugio en días agitados y una celebración de los momentos simples de la vida.<br>

    <br>Nuestra historia comienza con una vieja máquina de café heredada, de mi abuelo que era Italiano, y orgulloso servia el cafe para disfrutar en familia en la cocina de casa, donde las primeras tazas se servían entre risas, historias y largas charlas. De ese rincón pequeño y cálido, nació la idea de compartir con otros esa magia que el café nos regalaba cada día.<br>

    <br>Cada grano que seleccionamos, cada aroma que invade nuestro espacio, y cada taza que servimos lleva consigo el amor, la dedicación y el respeto por una tradición que atraviesa generaciones. Trabajamos de la mano con pequeños productores, apostando por prácticas justas y sustentables, porque creemos que cada paso en el camino importa.<br>

    <br>Leblanc no es solo un café, para nosotros es un hogar lejos de casa, un abrazo en forma de espresso, un espacio donde cada historia cuenta.<br>

    <br>Estamos aqui para demostrarle al mundo que realmente existimos, y que no estamos solos en el camino.<br>

    Gracias por acompañarnos en este viaje.
    Bienvenidos a nuestra familia.<br>
    Desde 1972 llevando el sabor de nuestro hogar en cada taza.


